/*
    ()=>{} -> Es una funcion anonima
*/
/*
    function nombre(){} -> Es una funcion con entorno
*/

/*
    Una funcion es una forma de declarar codigo en bloques
    Puede recibir datos de entrada que son conocidos como parametros
    Puede devolver datos de salida que son conocidos como resultados
*/

const suma = function(a, b){
    return a + b;
}

console.log(suma(1, 2));